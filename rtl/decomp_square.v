// =============================================================================
// Module  : decomp_square
// Description : 单通道解压缩 + 求模方，2拍流水
//
// 输入格式：cmp_data[19:0] = {exp[3:0], sign[0], data[14:0]}
//
// 流水线结构：
//   Stage 1（组合）：构造 val16，计算 prod = val16*val16，计算 shift_amt
//   寄存器：prod_reg[31:0], shift_amt_reg[4:0]（由顶层打拍）
//   Stage 2（组合）：square = {28'b0, prod_reg} << shift_amt_reg
//
// 注意：本模块为纯组合逻辑，流水寄存器由顶层 deu_design 负责。
//       顶层需在 Stage1 输出和 Stage2 输入之间插入寄存器。
//
// 位宽分析：
//   val16 最大 16'hFFFF；val16² 最大 32'hFFFE_0001；
//   shift_amt 最大 28；square 最大 ~ 2^59 < 2^64 ✓
// =============================================================================

module decomp_square (
    input  wire [19:0] cmp_data,
    output wire [63:0] square
);

    wire [3:0]  exp;
    wire [14:0] data;
    // sign = cmp_data[15]，平方运算与符号无关，不使用
    assign exp  = cmp_data[19:16];
    assign data = cmp_data[14:0];

    // -------------------------------------------------------------------------
    // 构造 val16：exp>0 时补隐含前导 '1'
    // -------------------------------------------------------------------------
    wire [15:0] val16;
    assign val16 = (|exp) ? {1'b1, data} : {1'b0, data};

    // -------------------------------------------------------------------------
    // 16x16 无符号乘法 → 32-bit 乘积
    // -------------------------------------------------------------------------
    wire [31:0] prod;
    assign prod = val16 * val16;

    // -------------------------------------------------------------------------
    // 移位量：2*(exp-1)，范围 0~28，5bit 可容纳（28 = 5'b11100）
    // -------------------------------------------------------------------------
    wire [4:0] shift_amt;
    assign shift_amt = (|exp) ? (({1'b0, exp} - 5'd1) << 1) : 5'd0;

    // -------------------------------------------------------------------------
    // 最终结果：64bit 内零扩展后左移
    // -------------------------------------------------------------------------
    assign square = {32'b0, prod} << shift_amt;

endmodule
